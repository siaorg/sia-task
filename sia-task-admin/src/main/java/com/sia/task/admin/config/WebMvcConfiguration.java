package com.sia.task.admin.config;

import com.sia.task.admin.filter.IndexRouteInterceptor;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.InterceptorRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

/**
 * @author: MAOZW
 * @Description: WebMvcConfiguration
 * @date 2018/4/1811:10
 */
@Configuration
public class WebMvcConfiguration extends WebMvcConfigurerAdapter {

    @Autowired
    IndexRouteInterceptor vueRouteInterceptor;

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        // addPathPatterns 用于添加拦截规则
        // excludePathPatterns 用户排除拦截
        // 映射为 user 的控制器下的所有映射
        registry.addInterceptor(vueRouteInterceptor).addPathPatterns("/error").excludePathPatterns("/index", "/");
        super.addInterceptors(registry);
    }

}
